'''
Our python script simply converts one data type to another.
Demonstrates how to declare variables.
'''

#convert to an integer (type coercion)
students = "16" #string
print(type(students))


students_int = int(students)
print(type(students_int))

students_float = float(students)
print(type(students_float)) #Float
print(students_float)
#16
#16.0


# str() -> convert to string
# int() -> convert to integer
# float() -> convert to a float
# bool() -> convert to a boolean (True or False)